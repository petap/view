    public function testBFS()
    {
        $graph = [
            1 => [
                'value' => '1',
                'data' => [
                    'd1' => 'v1'
                ],
                'children' => [
                    2 => [
                        'value' => '2',
                        'fromParent' => 'd1'
                    ],
                    3 => [
                        'value' => '3',
                        'children' => [
                            5 => [
                                'value' => '5',
                                'children' => [
                                    7 => [
                                        'value' => '7',
                                    ],
                                    8 => [
                                        'value' => '8',
                                    ],
                                ],
                            ],
                            6 => [
                                'value' => '6',
                            ],
                        ]
                    ],
                    4 => [
                        'value' => '4',
                    ],
                ]
            ],
        ];

        $startNode = 1;
        $views = [];

        $visited = array();
        $queue = new \SplQueue();

        $queue->enqueue($graph[$startNode]);
        $visited[] = $startNode;

        while ($queue->count() > 0) {
            $current = $queue->dequeue();

            if (!empty($current['children'] )) {
                foreach ($current['children'] as $viewId => $child) {

                    if (!in_array($viewId, $visited)) {
                        $child['parent'] = $current;
                        $queue->enqueue($child);
                    }

                    $visited[] = $viewId;
                }
            }

            $view = [
                'value' => $current['value'],
            ];
            if (isset($current['parent']) && isset($current['fromParent'])) {
                $view['data'][$current['fromParent']] = $current['parent']['data'][$current['fromParent']];
            }
            $views[] = $view;
        }

        $expected = [
            [
                'value' => '1',
            ],
            [
                'value' => '2',
                'data' => [
                    'd1' => 'v1'
                ],
            ],
            [
                'value' => '3',
            ],
            [
                'value' => '4',
            ],
            [
                'value' => '5',
            ],
            [
                'value' => '6',
            ],
            [
                'value' => '7',
            ],
            [
                'value' => '8',
            ],
        ];

        $this->assertEquals($expected, $views);
    }
